//: # Data Flow in SwiftUI

/*:
Data flow in SwiftUI is structured, and there are defined ways of passing data into child views, or from child view back up to the parent. The most obvious data flow is passing a value from the parent to a child view, or using a `Binding` to allow a child to send values back to the parent.

Things become more complex when you want to pass data data up or down the view hierarchy, and as your application grows having a large number of dependencies that are passed through the environment can add complexity.

